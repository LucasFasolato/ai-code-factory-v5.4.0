import fs from 'node:fs';
import path from 'node:path';
import { aiPath } from '../core/paths.js';
import { readJsonSafe, readText, writeText } from '../core/fs.js';
import { nowIso } from '../core/format.js';
import { readProjectMap } from './repo-map-engine.js';
import { lessonsSummary } from './history-engine.js';

// v5.0 AGENTS.md sync — the emerging cross-agent standard. Empirical studies
// associate its presence with ~16% lower agent runtime at comparable task
// completion. The harness compiles it from live knowledge (project map,
// standards, constraints, lessons) instead of leaving a stale hand-written
// file: static context is marginal, dynamic context that evolves with the
// project is what actually pays.

const MANAGED_START = '<!-- acf:managed:start -->';
const MANAGED_END = '<!-- acf:managed:end -->';

export function syncAgentsMd(root, options = {}) {
  const block = buildManagedBlock(root);
  const targets = options.targets || ['AGENTS.md', 'CLAUDE.md'];
  const written = [];
  for (const name of targets) {
    const file = path.join(root, name);
    const existing = fs.existsSync(file) ? readText(file, '') : '';
    const next = mergeManagedBlock(existing, block, name);
    writeText(file, next);
    written.push({ file: name, chars: next.length });
  }
  return { written, generated_at: nowIso() };
}

function buildManagedBlock(root) {
  const map = readProjectMap(root);
  const constraints = readJsonSafe(aiPath(root, 'constraints.json'), null);
  const dna = readJsonSafe(aiPath(root, 'project-dna.json'), null);
  const standards = collectStandards(root);
  let lessons = [];
  try { lessons = lessonsSummary(root) || []; } catch { lessons = []; }

  const lines = [MANAGED_START, `<!-- Generated by AI Code Factory on ${nowIso()}. Edit outside this block; run \`npm run ai -- agents-md sync\` to refresh. -->`, ''];
  lines.push('## Project overview');
  if (map) {
    lines.push(`- Frameworks: ${map.framework.join(', ') || 'unknown'}`);
    lines.push(`- Mapped modules: ${map.stats.files} files, ${map.stats.controllers} controllers, ${map.stats.services} services, ${map.stats.entities} entities, ${map.stats.routes} routes.`);
    const key = map.modules.filter((m) => ['controller', 'service', 'entity', 'nest-module'].includes(m.kind)).slice(0, 20);
    if (key.length) {
      lines.push('- Key modules:');
      for (const m of key) lines.push(`  - ${m.file} (${m.kind})${m.routes.length ? ` — ${m.routes.slice(0, 4).join(', ')}` : ''}`);
    }
  } else {
    lines.push('- Run `npm run ai -- repo-map` to generate the project map.');
  }
  if (dna) lines.push('', '## Project DNA', '```json', JSON.stringify(dna, null, 2).slice(0, 1500), '```');
  if (standards) lines.push('', '## Engineering standards (mandatory)', standards);
  if (constraints) lines.push('', '## Locked constraints', '```json', JSON.stringify(constraints, null, 2).slice(0, 1200), '```');
  if (Array.isArray(lessons) && lessons.length) {
    lines.push('', '## Lessons from previous work in this repo');
    for (const l of lessons.slice(0, 10)) lines.push(`- ${typeof l === 'string' ? l : (l.lesson || l.summary || JSON.stringify(l)).slice(0, 200)}`);
  }
  lines.push('', '## Working agreement for AI agents');
  lines.push('- Never implement directly on the base branch; every requirement lives on its own `acf/req-*` branch.');
  lines.push('- Never invent business data (emails, phones, metrics, clients). Use explicit placeholders.');
  lines.push('- Entity/schema changes require a migration in the same change.');
  lines.push('- Run lint, typecheck, tests and build before considering work done.');
  lines.push('- Stay inside the declared scope of the requirement; unrelated files are off-limits.');
  lines.push(MANAGED_END);
  return lines.join('\n');
}

function mergeManagedBlock(existing, block, name) {
  if (existing.includes(MANAGED_START) && existing.includes(MANAGED_END)) {
    const before = existing.slice(0, existing.indexOf(MANAGED_START));
    const after = existing.slice(existing.indexOf(MANAGED_END) + MANAGED_END.length);
    return `${before}${block}${after}`;
  }
  const header = existing.trim() ? `${existing.trim()}\n\n` : `# ${name === 'CLAUDE.md' ? 'Claude' : 'Agent'} instructions for this repository\n\n`;
  return `${header}${block}\n`;
}

function collectStandards(root) {
  const dir = aiPath(root, 'standards');
  if (!fs.existsSync(dir)) return '';
  const parts = [];
  for (const file of fs.readdirSync(dir).filter((f) => f.endsWith('.md')).slice(0, 5)) {
    const text = readText(path.join(dir, file), '').trim();
    if (text) parts.push(`### ${file}\n${text.slice(0, 1200)}`);
  }
  return parts.join('\n\n');
}
