import { aiPath, requestPaths } from '../core/paths.js';
import { readJsonSafe, readText, writeJson, writeText } from '../core/fs.js';
import { nowIso } from '../core/format.js';

export function generateApiContract(root, requestId) {
  const intake = readJsonSafe(requestPaths(root, requestId).intake, {});
  const ask = String(intake.raw_user_ask || intake.interpreted_intent || '').toLowerCase();
  const method = ask.includes('post') ? 'POST' : ask.includes('patch') ? 'PATCH' : ask.includes('delete') ? 'DELETE' : 'GET';
  const resource = (ask.match(/\/(\w[\w-]*)/) || [null, 'resource'])[1];
  const contract = {
    request_id: requestId,
    generated_at: nowIso(),
    method,
    path: ask.match(/\/\w[\w/-]*/)?.[0] || `/${resource}`,
    validation_required: true,
    auth_required: /admin|auth|jwt|user|private/.test(ask),
    query: method === 'GET' ? { page: 'optional number', limit: 'optional number' } : {},
    body: method !== 'GET' ? { dto: 'required DTO/schema' } : {},
    response: { data: 'defined by implementation contract' },
    errors: [400, 401, 403, 404, 500],
    tests_required: ['happy path', 'validation error', 'not found/permission when applicable']
  };
  const file = aiPath(root, 'contracts', 'api', `${requestId}-api-contract.json`);
  writeJson(file, contract);
  return { path: file, contract };
}

export function generateAdr(root, title, decision, context = '') {
  const dir = aiPath(root, 'adr');
  const id = `ADR-${String(Date.now()).slice(-6)}`;
  const safe = title.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '').slice(0, 60) || 'decision';
  const file = aiPath(root, 'adr', `${id}-${safe}.md`);
  const md = [`# ${id}: ${title}`, '', `Status: proposed`, `Date: ${nowIso()}`, '', '## Context', '', context || 'Generated by AI Code Factory.', '', '## Decision', '', decision, '', '## Consequences', '', '- Decision is auditable.', '- Future changes should update or supersede this ADR.'].join('\n');
  writeText(file, md);
  return { id, path: file, markdown: md };
}
